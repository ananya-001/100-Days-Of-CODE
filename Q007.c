// Q7 : Write a program to swap two numbers without using a third variable.

#include <stdio.h>
int main()
{
    int a,b;
    printf("Enter first number : ");
    scanf("%d", &a);
    printf("Enter the second number : ");
    scanf("%d",&b);
    printf("After swapping the number without using third variable :\n");
    printf("First number becomes : %d \n", b);
    printf("Second number becomes : %d",a);
}
